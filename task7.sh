#!/bin/bash
# Скрипт для демонстрації стандартних потоків та кодів повернення

# 1. Використовуємо stdout (потік 1) для запиту
echo "Введіть назву файлу для перевірки (stdin):"

# 2. Використовуємо stdin (потік 0) для отримання даних
read filename

# 3. Спроба вивести інформацію про файл. 
# stdout (1) направляємо у файл успіху, а stderr (2) — у файл помилок
if ls -l "$filename" 1> success.log 2> error.log; then
    # Якщо команда успішна, виводимо повідомлення та повертаємо код 0
    echo "Успіх! Інформацію про файл записано в success.log."
    exit 0
else
    # Якщо сталася помилка, виводимо її в потік stderr та повертаємо код 1
    echo "Помилка! Файл не знайдено. Деталі в error.log." >&2
    exit 1
fi
